package edu.escuelaing.arsw.proyecto.entities;

/**
 * Clase que representa el color de una figura por
 * medio de su codigo RGB
 */
public class Color {
    private int r;
    private int g;
    private int b;


    public int getR() {
        return r;
    }

    public void setR(int r) {
        this.r = r;
    }

    public int getG() {
        return g;
    }

    public void setG(int g) {
        this.g = g;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }
}
